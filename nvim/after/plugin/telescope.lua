local builtin = require('telescope.builtin')
vim.keymap.set('n', '<leader>ff', builtin.find_files, { desc = 'Telescope find files' })
vim.keymap.set('n', '<leader>sg', builtin.live_grep, { desc = 'Telescope live grep' })
vim.keymap.set('n', '<leader>gb', builtin.git_branches, { desc = 'Telescope git commits for current buffer' })
vim.keymap.set('n', '<leader>gC', builtin.git_commits, { desc = 'Telescope git commits for current buffer' })
vim.keymap.set('n', '<leader>gc', builtin.git_bcommits, { desc = 'Telescope git commits for current buffer' })
vim.keymap.set('n', '<leader>fb', builtin.buffers, { desc = 'Telescope buffers' })
vim.keymap.set('n', '<leader>fh', builtin.help_tags, { desc = 'Telescope help tags' })
vim.keymap.set('n', '<leader>fg', builtin.git_files, { desc = 'Telescope git tracked files' })
vim.keymap.set({'n','v'}, '<leader>s*', builtin.grep_string, { desc = 'Telescope grep currentselection' })
